import type { NextPage } from "next";
import Head from "next/head";
import { signIn, signOut, useSession } from "next-auth/react";
import Messages from "../components/messages";
import PostMessage from "../components/postMessage";

const Home: NextPage = (props) => {
  const { data: session, status } = useSession();
  console.log(props);
  if (status === "loading") {
    return (
      <main>
        <p>...Loading</p>
      </main>
    );
  }
  return (
    <>
      <Head>
        <title>GuestBook App!</title>
        <meta name="Guest Book App" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className="flex flex-col items-center pt-4">
        <h1 className="text-3xl pt-4">Guest Book</h1>
        <p className="mt-4 text-lg">
          Created with <code>create-t3-app</code>
        </p>

        {session ? (
          <div className="py-10">
            <div className="flex flex-col items-center">
              <p className="py-5 items-center">
                Well hello there{" "}
                <span className="text-violet-300">{session.user?.name}</span>,
                nice to see you!
              </p>
              <button
                className="bg-violet-900  hover:bg-violet-800 font-bold py-2 px-4 rounded-full "
                onClick={() => signOut()}
              >
                Logout
              </button>
            </div>
            <div className="pt-16">
              <PostMessage />
            </div>
            <div className="pt-12">
              <Messages />
            </div>
          </div>
        ) : (
          <div className="pt-5">
            <button
              className="bg-violet-900  hover:bg-violet-800 hover:bg-violet-900 font-bold py-2 px-4 rounded-full"
              onClick={() => signIn("discord")}
            >
              Sign In
            </button>
          </div>
        )}
      </main>
    </>
  );
};

export default Home;
